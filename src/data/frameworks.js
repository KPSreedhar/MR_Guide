export function validateFramework(framework) {
  const requiredFields = [
    'id', 'name', 'category', 'description', 
    'docsLink', 'difficulty', 'lastUpdated', 'ssrSupport'
  ];
  
  return requiredFields.every(field => {
    if (field === 'tags') return Array.isArray(framework[field]);
    return framework[field] !== undefined && framework[field] !== null;
  });
}